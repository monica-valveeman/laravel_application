     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required|alpha',
            'countrystuc_id'=>'required|Integer',
            'countrystuc_type'=>'required'
        ]);
            $country=new Country();
            $country->name=$request->name;
            $country->countrystuc_id=$request->countrystuc_id;
            $country->countrystuc_type=$request->countrystuc_type;
            $country->save();
            return redirect('/country');
    }

